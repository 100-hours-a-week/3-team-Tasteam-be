	/**
	 * subgroup.member_count를 단일 CASE WHEN UPDATE로 일괄 갱신한다.
	 * N번 UPDATE 대신 1번 UPDATE로 처리.
	 */
	@Transactional
	public void updateSubgroupMemberCounts(Map<Long, Integer> counts) {
		if (counts.isEmpty()) {
			return;
		}

		int n = counts.size();
		// 파라미터: WHEN ? THEN ? 가 n개, IN (?) 가 n개 → 총 3n개
		List<Object> params = new ArrayList<>(n * 3);
		List<Long> ids = new ArrayList<>(n);

		StringBuilder sql = new StringBuilder("UPDATE subgroup SET member_count = CASE id ");
		for (Map.Entry<Long, Integer> e : counts.entrySet()) {
			sql.append("WHEN ? THEN ? ");
			params.add(e.getKey());
			params.add(e.getValue());
			ids.add(e.getKey());
		}
		sql.append("ELSE member_count END WHERE id IN (");
		for (int i = 0; i < ids.size(); i++) {
			if (i > 0)
				sql.append(',');
			sql.append('?');
			params.add(ids.get(i));
		}
		sql.append(')');

		jdbcTemplate.update(sql.toString(), params.toArray());
	}

