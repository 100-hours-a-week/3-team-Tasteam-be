	// ── helpers ───────────────────────────────────────────────────────────────

	private Timestamp ts() {
		return Timestamp.from(Instant.now());
	}

	private long query(String sql) {
		Long result = jdbcTemplate.queryForObject(sql, Long.class);
		return result != null ? result : 0L;
	}
