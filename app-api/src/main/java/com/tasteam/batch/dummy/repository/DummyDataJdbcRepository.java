		// 1. 더미 멤버가 작성한 리뷰의 키워드 삭제
		jdbcTemplate.update("""
			DELETE FROM review_keyword
			WHERE review_id IN (
			    SELECT id FROM review
			    WHERE member_id IN (SELECT id FROM member WHERE email LIKE '%@dummy.tasteam.kr')
			)
			""");

		// 2. 더미 멤버가 작성한 리뷰 삭제
		jdbcTemplate.update("""
			DELETE FROM review
			WHERE member_id IN (SELECT id FROM member WHERE email LIKE '%@dummy.tasteam.kr')
			""");

		// 3. 더미 멤버의 그룹 가입 내역 삭제
		jdbcTemplate.update("""
			DELETE FROM group_member
			WHERE member_id IN (SELECT id FROM member WHERE email LIKE '%@dummy.tasteam.kr')
			""");

		// 4. 더미 멤버의 하위그룹 가입 내역 삭제
		jdbcTemplate.update("""
			DELETE FROM subgroup_member
			WHERE member_id IN (SELECT id FROM member WHERE email LIKE '%@dummy.tasteam.kr')
			""");

		// 5. 더미 멤버의 채팅방 멤버 내역 삭제
		jdbcTemplate.update("""
			DELETE FROM chat_room_member
			WHERE member_id IN (SELECT id FROM member WHERE email LIKE '%@dummy.tasteam.kr')
			""");

		// 6. 더미 그룹 산하 채팅방의 메시지 삭제
		jdbcTemplate.update("""
			DELETE FROM chat_message
			WHERE chat_room_id IN (
			    SELECT cr.id FROM chat_room cr
			    JOIN subgroup sg ON cr.subgroup_id = sg.id
			    WHERE sg.group_id IN (SELECT id FROM "group" WHERE name LIKE '더미그룹-%')
			)
			""");

		// 7. 더미 그룹 산하 채팅방의 멤버 내역 삭제 (위에서 남은 것 정리)
		jdbcTemplate.update("""
			DELETE FROM chat_room_member
			WHERE chat_room_id IN (
			    SELECT cr.id FROM chat_room cr
			    JOIN subgroup sg ON cr.subgroup_id = sg.id
			    WHERE sg.group_id IN (SELECT id FROM "group" WHERE name LIKE '더미그룹-%')
			)
			""");

		// 8. 더미 그룹 산하 채팅방 삭제
